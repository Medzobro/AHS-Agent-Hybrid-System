#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
    برنامج بسيط لطباعة الأرقام من 1 إلى 10
    ---------------------------------------------------------------
    هذا الكود يعرض دالة تطبع الأرقام من 1 إلى 10 مع معالجة الأخطاء
"""

def طباعة_الأرقام(بداية: int = 1, نهاية: int = 10) -> None:
    """
    تطبع الأرقام من `بداية` إلى `نهاية` (ضمناً) الواحد تلو الآخر.

    البارامترات:
        بداية (int): الرقم الذي نبدأ منه (افتراضي: 1)
        نهاية  (int): آخر رقم نطبع (افتراضي: 10)
    
    الإرجاع:
        None — فقط تطبع الأرقام على الشاشة

    الاستثناءات:
        ValueError: إذا كانت البداية أكبر من النهاية أو أي قيمة سالبة (حسب المنطق)
    """
    # التحقق من أن القيم المدخلة صحيحة منطقياً
    if بداية > نهاية:
        raise ValueError(
            f"❌ البداية ({بداية}) لا يمكن أن تكون أكبر من النهاية ({نهاية})"
        )

    if not isinstance(بداية, int) or not isinstance(نهاية, int):
        raise TypeError("⚠️ كلا القيمتين يجب أن تكونا من نوع Integer")

    print(f"\n🔢 طباعة الأرقام من {بداية} إلى {نهاية}:\n" + "-" * 30)

    # الحلقة الأساسية: تمر على كل عدد في النطاق وتطبعه
    for رقم in range(بداية, نهاية + 1):
        print(f"   {رقم}")

    print("-" * 30)
    print(f"✅ تم بنجاح! تم طباعة {نهاية - بداية + 1} أرقام.\n")


def مثال_استخدام() -> None:
    """
    دالة توضح كيفية استخدام الدالة `طباعة_الأرقام` بحالات مختلفة.
    """
    print("=" * 40)
    print("          📘 مثال 1 — الاستخدام الافتراضي (1 إلى 10)")
    print("=" * 40)
    طباعة_الأرقام()  # تستخدم القيم الافتراضية: بداية=1, نهاية=10

    print("=" * 40)
    print("          📗 مثال 2 — نطاق مخصص (5 إلى 15)")
    print("=" * 40)
    طباعة_الأرقام(بداية=5, نهاية=15)

    print("=" * 40)
    print("          📕 مثال 3 — رقم واحد فقط (7 إلى 7)")
    print("=" * 40)
    طباعة_الأرقام(7, 7)

    print("=" * 40)
    print("          📙 مثال 4 — معالجة الخطأ: البداية > النهاية")
    print("=" * 40)
    try:
        طباعة_الأرقام(10, 1)
    except ValueError as خطأ:
        print(f"🚫 تم التقاط الخطأ المتوقع: {خطأ}\n")


# —— نقطة الدخول الرئيسية ——
if __name__ == "__main__":
    """
    عندما يُشغَّل الملف مباشرة (وليس استيراده)،
    ننفذ `مثال_استخدام()` ليرى المستخدم كيف تعمل الدالة.
    """
    مثال_استخدام()