from typing import Union

# ------------------------------------------------
# الدالة الأساسية — تجمع رقمين وتدعم int و float
# ------------------------------------------------
def add(a: Union[int, float], b: Union[int, float]) -> Union[int, float]:
    """
    ترجع مجموع رقمين a و b.

    المعاملات:
        a (int | float): الرقم الأول
        b (int | float): الرقم الثاني

    الإرجاع:
        int | float: مجموع a و b

    الاستثناءات:
        TypeError: إذا كان أحد المعاملات من نوع غير رقمي
    """
    # TODO: هذه الجملة الشرطية تستخدم isinstance للتحقق من أن
    #      المدخلات من نوع int أو float فقط.
    if not (isinstance(a, (int, float)) and isinstance(b, (int, float))):
        raise TypeError(
            f"كلا المعاملين يجب أن يكونا أرقاماً (int أو float)، "
            f"ولكن تم تمرير: a={type(a).__name__}, b={type(b).__name__}"
        )

    # عملية الجمع البسيطة
    result = a + b

    # إذا كانت النتيجة عدداً صحيحاً تماماً،
    # نرجع int بدلاً من float للحصول على مخرجات أنظف.
    if isinstance(result, float) and result == int(result):
        return int(result)

    return result


